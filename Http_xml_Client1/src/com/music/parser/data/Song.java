package com.music.parser.data;

public class Song {

	private String songname;
	private String singer;
	private String cpname;
	private String productid;
	private String img_artist_url;
	private String img_album_url;
	private String wav_url;
	private String price;
	
	
	public void setSongName(String songname) {
		this.songname = songname;
	}
	public String getSongName() {
		return songname;
	}
	public void setSinger(String singer) {
		this.singer = singer;
	}
	public String getSinger() {
		return singer;
	}
	public void setCpname(String cpname) {
		this.cpname = cpname;
	}
	public String getCpname() {
		return cpname;
	}
	public void setProductid(String productid) {
		this.productid = productid;
	}
	public String getProductid() {
		return productid;
	}
	public void setImgArtistUrl(String img_artist) {
		this.img_artist_url = img_artist;
	}
	public String getImgArtistUrl() {
		return img_artist_url;
	}
	public void setImgAlbumUrl(String img_album) {
		this.img_album_url = img_album;
	}
	public String getImgAlbumUrl() {
		return img_album_url;
	}
	public void setWavUrl(String wav_url) {
		this.wav_url = wav_url;
	}
	public String getWavUrl() {
		return wav_url;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getPrice() {
		return price;
	}

	



}
